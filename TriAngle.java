public class TriAngle {
    private double base;
    private double height;
    public TriAngle(double d,double h){
        height = h;
        base = d;

    }
    public TriAngle(){

    }
    public double getBase(){
        return base;
    }
    public double getHeight(){
        return height;
    }
    public void setBase(double t){
        base = t;
    }
    public void setHeight(double t){
        height = t;
    }
}
